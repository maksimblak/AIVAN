from __future__ import annotations

from telegram import Update
from telegram.constants import ParseMode
from telegram.ext import ContextTypes
from telegram.helpers import escape_markdown


async def cmd_start(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """
    /start ‚Äî –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏–µ, –∫—Ä–∞—Ç–∫–æ–µ –æ–ø–∏—Å–∞–Ω–∏–µ –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç–µ–π –∏ –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏—è.
    """
    md2 = lambda t: escape_markdown(t, version=2)  # noqa: E731

    text = (
        f"üëã *{md2('–ü—Ä–∏–≤–µ—Ç!')}\n*\n"
        f"–Ø ‚Äî –±–æ—Ç —é—Ä–∏–¥–∏—á–µ—Å–∫–∏—Ö –∫–æ–Ω—Å—É–ª—å—Ç–∞—Ü–∏–π –Ω–∞ –±–∞–∑–µ GPT.\n\n"
        f"–ß—Ç–æ —É–º–µ—é:\n"
        f"‚Ä¢ {md2('–û—Ç–≤–µ—á–∞—Ç—å –Ω–∞ —é—Ä–∏–¥–∏—á–µ—Å–∫–∏–µ –≤–æ–ø—Ä–æ—Å—ã')} (‚öñÔ∏è)\n"
        f"‚Ä¢ {md2('–°—Ç—Ä—É–∫—Ç—É—Ä–∏—Ä–æ–≤–∞—Ç—å –æ—Ç–≤–µ—Ç—ã')} ‚Äî –∫—Ä–∞—Ç–∫–æ, –ø–æ–¥—Ä–æ–±–Ω–æ, –Ω–æ—Ä–º—ã –ø—Ä–∞–≤–∞\n"
        f"‚Ä¢ {md2('–§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞—Ç—å –æ—Ç–≤–µ—Ç')} –¥–ª—è —É–¥–æ–±–Ω–æ–≥–æ —á—Ç–µ–Ω–∏—è (—ç–º–æ–¥–∑–∏, —Å–ø–∏—Å–∫–∏)\n\n"
        f"–ö–∞–∫ –ø–æ–ª—å–∑–æ–≤–∞—Ç—å—Å—è:\n"
        f"1\\. {md2('–û—Ç–ø—Ä–∞–≤—å—Ç–µ —Ç–µ–∫—Å—Ç–æ–≤—ã–π –≤–æ–ø—Ä–æ—Å')} ‚Äî –Ω–∞–ø—Ä–∏–º–µ—Ä: _{md2('–ö–∞–∫ —Ä–∞—Å—Ç–æ—Ä–≥–Ω—É—Ç—å –¥–æ–≥–æ–≤–æ—Ä –∞—Ä–µ–Ω–¥—ã?')}_\n"
        f"2\\. –ü–æ–¥–æ–∂–¥–∏—Ç–µ –ø–∞—Ä—É —Å–µ–∫—É–Ω–¥ ‚Äî —è –Ω–∞–ø–∏—à—É üü° *{md2('–ø–µ—á–∞—Ç–∞–µ—Ç')}*\n"
        f"3\\. –ü–æ–ª—É—á–∏—Ç–µ –æ—Ñ–æ—Ä–º–ª–µ–Ω–Ω—ã–π –æ—Ç–≤–µ—Ç —Å –Ω–æ—Ä–º–∞–º–∏ –ø—Ä–∞–≤–∞ üìö\n\n"
        f"‚ö†Ô∏è *{md2('–í–∞–∂–Ω–æ')}*: –∫–æ–Ω—Å—É–ª—å—Ç–∞—Ü–∏—è –Ω–æ—Å–∏—Ç –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–æ–Ω–Ω—ã–π —Ö–∞—Ä–∞–∫—Ç–µ—Ä –∏ "
        f"–Ω–µ –∑–∞–º–µ–Ω—è–µ—Ç –ø—Ä–æ—Ñ–µ—Å—Å–∏–æ–Ω–∞–ª—å–Ω—É—é —é—Ä–∏–¥–∏—á–µ—Å–∫—É—é –ø–æ–º–æ—â—å."
    )

    await update.effective_message.reply_text(
        text, parse_mode=ParseMode.MARKDOWN_V2, disable_web_page_preview=True
    )


async def cmd_help(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
    """
    /help ‚Äî –∫—Ä–∞—Ç–∫–∞—è —Å–ø—Ä–∞–≤–∫–∞ –ø–æ —Ñ—É–Ω–∫—Ü–∏–æ–Ω–∞–ª—É –∏ –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏—è–º.
    """
    md2 = lambda t: escape_markdown(t, version=2)  # noqa: E731

    text = (
        f"üÜò *{md2('–ü–æ–º–æ—â—å')}:*\n\n"
        f"‚Ä¢ –°–ø—Ä–∞—à–∏–≤–∞–π—Ç–µ –æ–¥–Ω–∏–º —Å–æ–æ–±—â–µ–Ω–∏–µ–º ‚Äî —è —Ä–∞–∑–±–µ—Ä—É —Å—É—Ç—å –∏ —Å—Ñ–æ—Ä–º–∏—Ä—É—é –æ—Ç–≤–µ—Ç.\n"
        f"‚Ä¢ –ü–∏—à–∏—Ç–µ –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ: —Ç–∞–∫ —è –¥–∞–º –±–æ–ª–µ–µ —Ç–æ—á–Ω—É—é –∫–æ–Ω—Å—É–ª—å—Ç–∞—Ü–∏—é.\n"
        f"‚Ä¢ –õ–∏–º–∏—Ç: {md2('–Ω–µ—Å–∫–æ–ª—å–∫–æ –∑–∞–ø—Ä–æ—Å–æ–≤ –≤ —á–∞—Å')} per user (–∞–Ω—Ç–∏—Å–ø–∞–º).\n"
        f"‚Ä¢ –î–ª–∏–Ω–∞ —Å–æ–æ–±—â–µ–Ω–∏—è –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∞ Telegram; –¥–ª–∏–Ω–Ω—ã–µ –æ—Ç–≤–µ—Ç—ã —è —Ä–∞–∑—Ä–µ–∑–∞—é –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏.\n\n"
        f"–ö–æ–º–∞–Ω–¥—ã:\n"
        f"/start ‚Äî –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–∏–µ\n"
        f"/help ‚Äî –ø–æ–º–æ—â—å\n"
    )
    await update.effective_message.reply_text(
        text, parse_mode=ParseMode.MARKDOWN_V2, disable_web_page_preview=True
    )
