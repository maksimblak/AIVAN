def render_legal_html(
    raw_text: str,
    *,
    underline_keywords: list[str] | None = None,
    auto_keywords: bool = True,
    max_keywords: int = 8,
) -> str:
    """
    –£–Ω–∏–≤–µ—Ä—Å–∞–ª—å–Ω—ã–π —Ä–µ–Ω–¥–µ—Ä –¥–ª—è –õ–Æ–ë–û–ì–û —Ç–µ–∫—Å—Ç–∞ (–±–µ–∑ —Å–ª–æ–≤–∞—Ä–µ–π):
    - –∞–≤—Ç–æ-–∑–∞–≥–æ–ª–æ–≤–∫–∏ (–ø–æ —Ñ–æ—Ä–º–µ —Å—Ç—Ä–æ–∫–∏),
    - —Ä–∞–∑—Ä—ã–≤ –≤—Å—Ç—Ä–æ–µ–Ω–Ω—ã—Ö —Å–ø–∏—Å–∫–æ–≤: -, ‚Ä¢, *, 1), 1. (–¥–∞–∂–µ –µ—Å–ª–∏ —Å—Ç–æ—è—Ç –ø–æ—Å–ª–µ —Å–ª–æ–≤–∞),
    - —Ä–∞–∑—Ä—ã–≤ —Å–∫–ª–µ–µ–∫ —Ç–∏–ø–∞ "...—Å–ª–æ–≤–æ–°–ª–µ–¥—É—é—â–µ–µ" (–±—É–∫–≤–∞ + –ó–∞–≥–ª–∞–≤–Ω–∞—è + —Å—Ç—Ä–æ—á–Ω–∞—è),
    - –∂–∏—Ä–Ω–µ–Ω–∏–µ "–¢–µ—Ä–º–∏–Ω: –ø–æ—è—Å–Ω–µ–Ω–∏–µ",
    - markdown-—Å—Å—ã–ª–∫–∏ –∏ –∞–≤—Ç–æ—Å—Å—ã–ª–∫–∏,
    - –∞–∫–∫—É—Ä–∞—Ç–Ω—ã–µ –∞–±–∑–∞—Ü—ã <br><br>.
    """
    t = _normalize(raw_text)
    if not t:
        return "‚Äî"

    # ---------- –°–¢–†–£–ö–¢–£–†–ù–´–ï –†–ê–ó–†–´–í–´ ----------
    # 1) –ü–æ—Å–ª–µ –∑–Ω–∞–∫–æ–≤ –ø—Ä–µ–ø–∏–Ω–∞–Ω–∏—è –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è –º–∞—Ä–∫–µ—Ä —Å–ø–∏—Å–∫–∞ -> –ø–µ—Ä–µ–Ω–æ—Å
    t = re.sub(r'([:;.,)\]])\s*(?=[\-‚Äì‚Äî*‚Ä¢]\s+)', r'\1\n', t)
    # 2) –ü–æ—Å–ª–µ —Ç–µ—Ö –∂–µ –∑–Ω–∞–∫–æ–≤ –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è –Ω—É–º–µ—Ä–∞—Ü–∏—è "1) " / "1. " -> –ø–µ—Ä–µ–Ω–æ—Å
    t = re.sub(r'([:;.,)\]])\s*(?=\d+[\.\)]\s+)', r'\1\n', t)
    # 3) –ù–µ—Å–∫–æ–ª—å–∫–æ –ø—Ä–æ–±–µ–ª–æ–≤ –∏ –¥–∞–ª–µ–µ –º–∞—Ä–∫–µ—Ä -> –ø–µ—Ä–µ–Ω–æ—Å
    t = re.sub(r'(?<!^)\s{2,}(?=[\-‚Äì‚Äî*‚Ä¢]\s+)', '\n', t)
    # 4) –°–ª—É—á–∞–π "‚Ä¶—Å–ª–æ–≤–æ- –ü—É–Ω–∫—Ç": –ø–µ—Ä–µ–Ω–æ—Å –ø–µ—Ä–µ–¥ "- " –µ—Å–ª–∏ –¥–∞–ª—å—à–µ –ó–∞–≥–ª–∞–≤–Ω–∞—è (—á—Ç–æ–±—ã "–ö—Ä–∞—Ç–∫–∏–π –æ—Ç–≤–µ—Ç- –ü—É–Ω–∫—Ç" –Ω–µ —Å–ª–∏–ø–∞–ª—Å—è)
    t = re.sub(r'(?<=\S)\s*(?=-\s+[A-Z–ê-–Ø–Å])', '\n', t)
    # 5) –í—Å—Ç—Ä–æ–µ–Ω–Ω—ã–µ "‚Ä¢ " –±–µ–∑ –ø—Ä–µ–ø–∏–Ω–∞–Ω–∏—è –ø–µ—Ä–µ–¥ –Ω–∏–º–∏ -> –ø–µ—Ä–µ–Ω–æ—Å
    t = re.sub(r'(?<!^)(?<!\n)\s*(?=‚Ä¢\s+)', '\n', t)
    # 6) –†–∞–∑—Ä—ã–≤ —Å–∫–ª–µ–µ–∫ –≤–∏–¥–∞ "‚Ä¶–æ—Ç–≤–µ—Ç–î–∞," (–±—É–∫–≤–∞ + –ó–∞–≥–ª–∞–≤–Ω–∞—è + —Å—Ç—Ä–æ—á–Ω–∞—è)
    t = re.sub(r'(?<=[A-Za-z–ê-–Ø–∞-—è–Å—ë])(?=[A-Z–ê-–Ø–Å][a-z–∞-—è—ë])', '\n', t)
    # -----------------------------------------

    # –∞–≤—Ç–æ–∫–ª—é—á–∏ (–ø–æ –∂–µ–ª–∞–Ω–∏—é)
    ukeys = underline_keywords
    if ukeys is None and auto_keywords:
        ukeys = _auto_keywords(t, limit=max_keywords)
    if ukeys is None:
        ukeys = []

    lines = t.split("\n")
    out: list[str] = []
    prev_blank = True

    for line in lines:
        raw = line.rstrip()

        # –ø—É—Å—Ç–∞—è —Å—Ç—Ä–æ–∫–∞ -> –∞–±–∑–∞—Ü
        if not raw:
            if not prev_blank:
                out.append("<br>")
            prev_blank = True
            continue

        s = raw

        # –ó–∞–≥–æ–ª–æ–≤–∫–∏ –ø–æ —Ñ–æ—Ä–º–µ (–∫–æ—Ä–æ—Ç–∫–∞—è —Å—Ç—Ä–æ–∫–∞ / "1) ..."/"1. ..."/ —Å—Ç—Ä–æ–∫–∞ –æ–∫–∞–Ω—á–∏–≤–∞–µ—Ç—Å—è ":")
        if _is_heading(s):
            s2 = re.sub(r"^\d+[\.\)]\s+", "", s).strip()
            out.append(f"<b>{html_escape(s2)}</b>")
            out.append("<br>")  # –Ω–µ–±–æ–ª—å—à–æ–π ¬´–≤–æ–∑–¥—É—Ö¬ª –ø–æ—Å–ª–µ –∑–∞–≥–æ–ª–æ–≤–∫–∞
            prev_blank = False
            continue

        # –°–ø–∏—Å–∫–∏ / –Ω—É–º–µ—Ä–∞—Ü–∏—è
        bullet = ""
        m_num = re.match(r"^(\d+[\.\)])\s+(.*)$", s)
        if m_num:
            bullet = f"{m_num.group(1)} "
            s = m_num.group(2)
        elif re.match(r"^([\-‚Äì‚Äî*‚Ä¢])\s+(.*)$", s):
            s = re.sub(r"^([\-‚Äì‚Äî*‚Ä¢])\s+", "", s, count=1)
            bullet = "‚Ä¢ "

        # –≠–∫—Ä–∞–Ω–∏—Ä—É–µ–º HTML
        e = html_escape(s)

        # "–¢–µ—Ä–º–∏–Ω: –ø–æ—è—Å–Ω–µ–Ω–∏–µ" -> –∂–∏—Ä–Ω—ã–π —Ç–µ—Ä–º–∏–Ω
        m_term = re.match(r"^([^:\n]{3,80}:\s*)(.*)$", e)
        if m_term:
            e = f"<b>{m_term.group(1)}</b>{m_term.group(2)}"

        # (–æ–ø—Ü–∏–æ–Ω–∞–ª—å–Ω–æ) –ø–æ–¥—á—ë—Ä–∫–∏–≤–∞–Ω–∏–µ –∞–≤—Ç–æ–∫–ª—é—á–µ–π ‚Äî —ç—Ç–æ —á–∞—Å—Ç–æ—Ç–Ω—ã–µ —Ñ–æ—Ä–º—ã, –Ω–µ —Å–ª–æ–≤–∞—Ä—å
        for kw in ukeys:
            if not kw:
                continue
            safe = html_escape(kw)
            e = re.sub(
                rf"(?iu)(?<![A-Za-z–ê-–Ø–∞-—è–Å—ë0-9\-])({re.escape(safe)})(?![A-Za-z–ê-–Ø–∞-—è–Å—ë0-9\-])",
                r"<u>\1</u>",
                e,
            )

        # Markdown-—Å—Å—ã–ª–∫–∏ -> <a>
        e = re.sub(
            r"\[([^\]]{1,200})\]\((https?://[^\s)]+)\)",
            lambda m: f'<a href="{html_escape(m.group(2), quote=True)}">{html_escape(m.group(1))}</a>',
            e,
        )
        # –ê–≤—Ç–æ—Å—Å—ã–ª–∫–∏
        e = _linkify(e)

        out.append((bullet + e).strip())
        prev_blank = False

    # –°–∫–ª–µ–π–∫–∞ + –Ω–æ—Ä–º–∞–ª–∏–∑–∞—Ü–∏—è –∞–±–∑–∞—Ü–µ–≤
    html_parts: list[str] = []
    for piece in out:
        if piece == "<br>":
            if html_parts and html_parts[-1] != "<br><br>":
                html_parts.append("<br><br>")
            continue
        if html_parts:
            html_parts.append("<br>")
        html_parts.append(piece)

    result = "".join(html_parts).strip()
